            id.style.display = "none";
        }
        function show_slide(id){
            id.style.display= "block";
        }
        const slideNext = ()=>{
            hide_slide(slide[i]);
            i++;
            if(i===slide.length){
                i = 0 ;
            }
            show_slide(slide[i]);
        }
        setInterval(function(){ slideNext()},5000);
        
